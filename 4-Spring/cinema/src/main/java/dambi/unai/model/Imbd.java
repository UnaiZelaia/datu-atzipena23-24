package dambi.unai.model;

public class Imbd {
    private int id;
    private double rating;
    private int votes;


    public Imbd() {
    }


    public int getId() {
        return id;
    }


    public void setId(int id) {
        this.id = id;
    }


    public double getRating() {
        return rating;
    }


    public void setRating(double rating) {
        this.rating = rating;
    }


    public int getVotes() {
        return votes;
    }


    public void setVotes(int votes) {
        this.votes = votes;
    }


}
